void solve(){
    ll n;
    cin >> n;

    vec a(n);
    vec b(n+1);

    scan(a,n);
    scan(b,n+1);

    ll ans = 1e9;

    for(int i=0;i<n;i++){
        ans = min(ans, abs(a[i] - b[n]));
        ans = min(ans, abs(b[i] - b[n]));

        if(min(a[i],b[i]) <= b[n] && max(a[i],b[i]) >= b[n]){
            ans=0;
        }
    }

    for(int i=0;i<n;i++){
        ans += abs(a[i] - b[i]);
    }

    cout << ans + 1;

    
}

